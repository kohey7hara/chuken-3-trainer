const fs = require("fs");
const vm = require("vm");

const contentContext = {};
vm.createContext(contentContext);
vm.runInContext(`${fs.readFileSync("data.js", "utf8")}; this.learningContent = learningContent;`, contentContext);

const existingVocabulary = contentContext.learningContent.cards.filter((card) => card.type === "Vocabulary");
const existingHanzi = new Set(existingVocabulary.map((card) => card.hanzi));
const targetImportedCount = 2000 - existingVocabulary.length;

const files = [1, 2, 3, 4, 5].map((level) => `/private/tmp/hsk-level-${level}.json`);
const imported = [];
const seen = new Set(existingHanzi);

for (const [fileIndex, file] of files.entries()) {
  const level = fileIndex + 1;
  const words = JSON.parse(fs.readFileSync(file, "utf8"));
  for (const word of words) {
    if (!word.hanzi || seen.has(word.hanzi)) continue;
    seen.add(word.hanzi);
    imported.push({
      source: `HSK ${level}`,
      hanzi: word.hanzi,
      pinyin: word.pinyin || "",
      answer: (word.translations || []).join("; "),
    });
    if (imported.length >= targetImportedCount) break;
  }
  if (imported.length >= targetImportedCount) break;
}

if (imported.length !== targetImportedCount) {
  throw new Error(`Expected ${targetImportedCount} imported words, got ${imported.length}`);
}

const allAnswers = imported.map((word) => word.answer);

function choicesFor(index, answer) {
  const choices = [answer];
  let offset = 17;
  while (choices.length < 4) {
    const candidate = allAnswers[(index + offset) % allAnswers.length];
    if (candidate && !choices.includes(candidate)) choices.push(candidate);
    offset += 29;
  }
  return choices;
}

const cards = imported.map((word, index) => ({
  lesson: 100 + Math.floor(index / 50) + 1,
  type: "Vocabulary",
  title: `${word.source} 単語`,
  hanzi: word.hanzi,
  pinyin: word.pinyin,
  prompt: "意味を選んでください。",
  choices: choicesFor(index, word.answer),
  answer: word.answer,
  note: `${word.source}由来の語彙です。日本語訳は今後の精査対象です。`,
}));

const output = `// Generated by scripts/build-vocab-2000.js
// Source: https://github.com/clem109/hsk-vocabulary
// License: MIT, Copyright (c) 2018 Clement Venard
const hskVocabularyCards = ${JSON.stringify(cards, null, 2)};
`;

fs.writeFileSync("vocab-2000.js", output);
console.log(`Generated ${cards.length} imported vocabulary cards.`);
