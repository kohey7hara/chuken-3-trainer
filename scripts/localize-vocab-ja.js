function parseLevel(card) {
  const match = card.title.match(/HSK (\d)/);
  return match ? Number(match[1]) : 9;
}

function cleanGloss(gloss) {
  return gloss
    .replace(/\([^)]*\)/g, "")
    .replace(/CL:.*/g, "")
    .replace(/Taiwan pr\..*/g, "")
    .replace(/abbr\..*/g, "")
    .replace(/\s+/g, " ")
    .trim();
}

function firstUsefulGloss(answer) {
  return answer
    .split(";")
    .map(cleanGloss)
    .find((part) => part && !part.includes("|") && !part.includes("["));
}

function convertCharacters(text) {
  return [...text].map((char) => simplifiedToJapanese[char] || char).join("");
}

function translateGloss(card) {
  if (zhJa[card.hanzi]) return zhJa[card.hanzi];
  const gloss = firstUsefulGloss(card.answer);
  if (!gloss) return convertCharacters(card.hanzi);
  if (firstGlossJa[gloss]) return firstGlossJa[gloss];

  const toMatch = gloss.match(/^to ([a-z]+)(?: |$)/i);
  if (toMatch && verbJa[toMatch[1]]) return verbJa[toMatch[1]];

  const nounMatch = gloss.match(/^(?:a |an |the )?([a-z ]+)$/i);
  if (nounMatch && firstGlossJa[nounMatch[1]]) return firstGlossJa[nounMatch[1]];

  return convertCharacters(card.hanzi);
}

function score(card, index) {
  const level = parseLevel(card);
  let value = level * 10000 + index;
  if (priorityWords.has(card.hanzi)) value -= 6000;
  if (card.hanzi.length <= 2) value -= 1000;
  if (/^(to |not|no|who|what|where|when|why|how)/i.test(firstUsefulGloss(card.answer) || "")) value -= 600;
  return value;
}

const localized = context.cards
  .map((card, index) => {
    const answer = translateGloss(card);
    return {
      ...card,
      answer,
      choices: card.choices.map((choice) => translateGloss({ ...card, answer: choice, hanzi: "" })),
      note: `${card.title.replace(" 単語", "")}由来。中検3級向けの日本語訳に整備中です。`,
      priority: score(card, index),
    };
  })
  .sort((a, b) => a.priority - b.priority)
  .map(({ priority, ...card }) => {
    const choices = [...new Set([card.answer, ...card.choices])].slice(0, 4);
    while (choices.length < 4) choices.push("関連語");
    return { ...card, choices };
  });

